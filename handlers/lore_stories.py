TOTAL_SCROLLS = len(SACRED_SCROLLS)

# -----------------------------------------------------------
# Клавиатура для листания свитков
# -----------------------------------------------------------
def get_scroll_nav_keyboard(index: int) -> InlineKeyboardBuilder:
    builder = InlineKeyboardBuilder()
    # Кнопки навигации
    builder.button(text="⬅️ Назад", callback_data=f"scroll_prev:{index}")
    builder.button(text="🎲 Случайный", callback_data="scroll_random")
    builder.button(text="Вперёд ➡️", callback_data=f"scroll_next:{index}")
    # Строчка с номером свитка
    builder.button(text=f"📜 {index+1}/{TOTAL_SCROLLS}", callback_data="scroll_info")
    # Кнопка "Главное меню"
    builder.button(text="🏠 Главное меню", callback_data="main_menu")
    builder.adjust(3, 1, 1)  # 3 кнопки в ряд, потом одна, потом одна
    return builder.as_markup()

# -----------------------------------------------------------
# Обработчики
# -----------------------------------------------------------

async def show_scroll(event_or_message, index: int):
    """Отображает свиток по индексу и навигационную клавиатуру."""
    if 0 <= index < TOTAL_SCROLLS:
        scroll = SACRED_SCROLLS[index]
        text = f"*{scroll['title']}*\n\n{scroll['text']}"
    else:
        text = "Свиток не найден."
    markup = get_scroll_nav_keyboard(index)

    if isinstance(event_or_message, Message):
        await event_or_message.answer(text, parse_mode="Markdown", reply_markup=markup)
    else:  # CallbackQuery
        try:
            await event_or_message.message.edit_text(text, parse_mode="Markdown", reply_markup=markup)
        except Exception:
            await event_or_message.answer()

@router.message(Command("stories"))
@router.callback_query(F.data == "magister_story")
async def start_stories(event: Message | CallbackQuery):
    """Открывает первый свиток или обрабатывает команду."""
    # Показываем первый свиток (индекс 0)
    await show_scroll(event, 0)

@router.callback_query(F.data.startswith("scroll_prev:"))
async def scroll_prev(callback: CallbackQuery):
    index = int(callback.data.split(":")[1])
    new_index = (index - 1) % TOTAL_SCROLLS
    await show_scroll(callback, new_index)
    await callback.answer()

@router.callback_query(F.data.startswith("scroll_next:"))
async def scroll_next(callback: CallbackQuery):
    index = int(callback.data.split(":")[1])
    new_index = (index + 1) % TOTAL_SCROLLS
    await show_scroll(callback, new_index)
    await callback.answer()

@router.callback_query(F.data == "scroll_random")
async def scroll_random(callback: CallbackQuery):
    import random
    index = random.randint(0, TOTAL_SCROLLS - 1)
    await show_scroll(callback, index)
    await callback.answer("🎲 Магистр выбрал случайный свиток")

@router.callback_query(F.data == "scroll_info")
async def scroll_info(callback: CallbackQuery):
    await callback.answer(f"Всего свитков: {TOTAL_SCROLLS}", show_alert=True)